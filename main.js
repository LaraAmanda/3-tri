function soma(num1,num2){
    return num1+num2;
}
console.log(soma(2,3));
function multiplication(num3,num3){
    return num3*num3;
}
console.log(multiplication(3,3));
function areaquadrado(lado){
    return lado**2;
}
console.log(areaquadrado(3));
let areatrapezio = function(base1,base2,altura){
    return base1+base2*altura/2;
}
console.log(areatrapezio(2,3,2));

let media = function(num1,num2,num3){
return (num1*num2*num3)/3;
}
console.log(media(2,3,5));
